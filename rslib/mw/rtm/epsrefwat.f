C ==========================================================================
C     ----------------------------------------------------------------------
C     Instead of "hard" coding this subroutine, it is incorporated into
C     the former refwat.f and changed the name to epsrefwat.f        [BTJ]
C     ----------------------------------------------------------------------
      subroutine epsw(f,t,epsr,epsi)
*     computes the complex dielectric value of pure water as a function
*     of frequency F (GHz), temperature T (deg. C)., using the 
*     model of Liebe et al. 1991
*     EPSR and EPSI are real and imaginary parts of dielectric const.
      real f,t,epsr,epsi
*
      theta = 1.0 - 300./(273.15 + t)
      e0 = 77.66 - 103.3*theta
      e1 = 0.0671*e0
      gam1 = 20.20 + 146.4*theta + 316.0*theta*theta
      e2 = 3.52 + 7.52*theta
      gam2 = 39.8*gam1
*
      fog1 = f/gam1
      fog2 = f/gam2
      fogg1 = fog1*fog1
      fogg2 = fog2*fog2

      epsr = (e0-e1)/(1 + fogg1) + 
     &     (e1-e2)/(1 + fogg2) + e2
      epsi = (e0-e1)*fog1/(1 + fogg1) +
     &     (e1-e2)*fog2/(1 + fogg2)
      return
      end









